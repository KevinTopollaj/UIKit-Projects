import UIKit

extension UIImage {
  static let arrowLeftArrowRightCircle = UIImage(systemName: "arrow.left.arrow.right.circle")
  static let ellipsisCircle = UIImage(systemName: "ellipsis.circle")
  static let arrowDownBackwardCircle = UIImage(systemName: "arrow.down.backward.circle",
                                               withConfiguration: UIImage.SymbolConfiguration(weight: .light))
  static let arrowUpForwardCircle = UIImage(systemName: "arrow.up.forward.circle",
                                            withConfiguration: UIImage.SymbolConfiguration(weight: .light))
  static let zeroCircle = UIImage(systemName: "0.circle",
                                  withConfiguration: UIImage.SymbolConfiguration(weight: .light))
}
